package fr.utc.dsi.pia.edt;

public class FinitionTemplate {
	
	private String idFinition;
	private String jante;
	private String nomFinition;
	private String prixFinition;
	
	public String getIdFinition() {
		return idFinition;
	}
	public void setIdFinition(String idFinition) {
		this.idFinition = idFinition;
	}
	public String getJante() {
		return jante;
	}
	public void setJante(String jante) {
		this.jante = jante;
	}
	public String getNomFinition() {
		return nomFinition;
	}
	public void setNomFinition(String nomFinition) {
		this.nomFinition = nomFinition;
	}
	public String getPrixFinition() {
		return prixFinition;
	}
	public void setPrixFinition(String prixFinition) {
		this.prixFinition = prixFinition;
	}


}