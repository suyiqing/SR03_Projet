package fr.utc.dsi.pia.edt;

public class CouleurTemplate {
	
	private String idCouleur;
	private String couleur;
	private String prixCouleur;
	
	public String getIdCouleur() {
		return idCouleur;
	}
	public void setIdCouleur(String idCouleur) {
		this.idCouleur = idCouleur;
	}
	public String getCouleur() {
		return couleur;
	}
	public void setCouleur(String couleur) {
		this.couleur = couleur;
	}
	public String getPrixCouleur() {
		return prixCouleur;
	}
	public void setPrixCouleur(String prixCouleur) {
		this.prixCouleur = prixCouleur;
	}

}
