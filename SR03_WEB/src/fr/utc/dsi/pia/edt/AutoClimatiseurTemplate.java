package fr.utc.dsi.pia.edt;

public class AutoClimatiseurTemplate {
	private String idAutoClimatiseur;

	private String autoclimatiseur;

	private String prix;

	public String getIdAutoClimatiseur() {
		return idAutoClimatiseur;
	}

	public void setIdAutoClimatiseur(String idAutoClimatiseur) {
		this.idAutoClimatiseur = idAutoClimatiseur;
	}

	public String getAutoclimatiseur() {
		return autoclimatiseur;
	}

	public void setAutoclimatiseur(String autoclimatiseur) {
		this.autoclimatiseur = autoclimatiseur;
	}

	public String getPrix() {
		return prix;
	}

	public void setPrix(String prix) {
		this.prix = prix;
	}
}
