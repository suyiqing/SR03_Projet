package fr.utc.dsi.pia.edt;

public class MotorisationTemplate {

	private String idMotorisation;
	private String motorisation;
	private String prixMotorisation;
	
	public String getIdMotorisation() {
		return idMotorisation;
	}
	public void setIdMotorisation(String idMotorisation) {
		this.idMotorisation = idMotorisation;
	}
	public String getMotorisation() {
		return motorisation;
	}
	public void setMotorisation(String motorisation) {
		this.motorisation = motorisation;
	}
	public String getPrixMotorisation() {
		return prixMotorisation;
	}
	public void setPrixMotorisation(String prixMotorisation) {
		this.prixMotorisation = prixMotorisation;
	}
	
}
