package fr.utc.dsi.pia.edt;

public class NavigateurTemplate {
	private String idNavigateur;

	private String navigateur;

	private String prix;

	public String getIdNavigateur() {
		return idNavigateur;
	}

	public void setIdNavigateur(String idNavigateur) {
		this.idNavigateur = idNavigateur;
	}

	public String getNavigateur() {
		return navigateur;
	}

	public void setNavigateur(String navigateur) {
		this.navigateur = navigateur;
	}

	public String getPrix() {
		return prix;
	}

	public void setPrix(String prix) {
		this.prix = prix;
	}
}
